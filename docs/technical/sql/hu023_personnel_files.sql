    SELECT DISTINCT public_id AS tenant_id
    FROM companies
), catalog_source AS (
    SELECT * FROM (VALUES
        ('MaritalStatus', 'SINGLE', 'Single', 10),
        ('MaritalStatus', 'MARRIED', 'Married', 20),
        ('MaritalStatus', 'DIVORCED', 'Divorced', 30),
