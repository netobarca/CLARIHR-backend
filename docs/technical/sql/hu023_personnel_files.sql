        ('IdentificationType', 'DUI', 'Documento Unico de Identidad', 10),
        ('IdentificationType', 'NIT', 'Numero de Identificacion Tributaria', 20),
        ('IdentificationType', 'NUP', 'Numero Unico Previsional', 30),
        ('IdentificationType', 'PASSPORT', 'Passport', 40),
        ('Bank', 'BANCO_AGRICOLA', 'Banco Agricola', 10),
        ('Bank', 'BANCO_CUSCATLAN', 'Banco Cuscatlan', 20),
        ('Currency', 'USD', 'US Dollar', 10),
        ('Currency', 'EUR', 'Euro', 20),
        ('BankAccountType', 'SAVINGS', 'Savings', 10),
        ('BankAccountType', 'CHECKING', 'Checking', 20),
        ('Hobby', 'READING', 'Reading', 10),
        ('Hobby', 'SPORTS', 'Sports', 20),
        ('Association', 'ASSOCIACION_SOLIDARIA', 'Asociacion Solidaria', 10),
        ('Association', 'COOPERATIVA', 'Cooperativa', 20)
    ) AS source(category, code, name, sort_order)
)
