CREATE TABLE IF NOT EXISTS personnel_files (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    record_type character varying(20) NOT NULL,
    first_name character varying(100) NOT NULL,
    last_name character varying(100) NOT NULL,
    full_name character varying(201) NOT NULL,
    normalized_full_name character varying(201) NOT NULL,
    birth_date timestamp with time zone NOT NULL,
    marital_status character varying(80),
    profession character varying(120),
    nationality character varying(120),
    personal_email character varying(320),
    institutional_email character varying(320),
    personal_phone character varying(40),
    institutional_phone character varying(40),
    birth_country character varying(120),
    birth_department character varying(120),
    birth_municipality character varying(120),
    photo_url character varying(1000),
    org_unit_public_id uuid,
    custom_data jsonb,
    is_active boolean NOT NULL,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_personnel_files PRIMARY KEY (id)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_files__public_id
    ON personnel_files (public_id);

CREATE INDEX IF NOT EXISTS ix_personnel_files__tenant_name
    ON personnel_files (tenant_id, normalized_full_name);

CREATE INDEX IF NOT EXISTS ix_personnel_files__tenant_type_active
    ON personnel_files (tenant_id, record_type, is_active);

CREATE INDEX IF NOT EXISTS ix_personnel_files__tenant_org_unit
    ON personnel_files (tenant_id, org_unit_public_id);

CREATE INDEX IF NOT EXISTS ix_personnel_files__tenant_birth_date
    ON personnel_files (tenant_id, birth_date);

CREATE TABLE IF NOT EXISTS personnel_file_identifications (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    personnel_file_id bigint NOT NULL,
    identification_type character varying(80) NOT NULL,
    identification_number character varying(80) NOT NULL,
    normalized_identification_number character varying(80) NOT NULL,
    issued_date timestamp with time zone,
    expiry_date timestamp with time zone,
    issuer character varying(200),
    is_primary boolean NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_personnel_file_identifications PRIMARY KEY (id),
    CONSTRAINT fk_personnel_file_identifications__personnel_file
        FOREIGN KEY (personnel_file_id)
        REFERENCES personnel_files (id)
        ON DELETE CASCADE,
    CONSTRAINT ck_personnel_file_identifications__issued_expiry
        CHECK (expiry_date IS NULL OR issued_date IS NULL OR expiry_date >= issued_date)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_identifications__public_id
    ON personnel_file_identifications (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_identifications__tenant_type_number
    ON personnel_file_identifications (tenant_id, identification_type, normalized_identification_number);

CREATE INDEX IF NOT EXISTS ix_personnel_file_identifications__tenant_file
    ON personnel_file_identifications (tenant_id, personnel_file_id);

