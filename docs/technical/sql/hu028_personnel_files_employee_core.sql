WITH employee_files AS (
    SELECT id,
           public_id,
           tenant_id,
           is_active,
           created_utc
    FROM personnel_files
    WHERE record_type = 'Employee'
), pending AS (
    SELECT file.*
    FROM employee_files file
    WHERE NOT EXISTS (
        SELECT 1
        FROM personnel_file_employee_profiles profile
        WHERE profile.personnel_file_id = file.id
          AND profile.tenant_id = file.tenant_id)
)
INSERT INTO personnel_file_employee_profiles (
    public_id,
    personnel_file_id,
    employee_code,
    normalized_employee_code,
    employment_status_code,
    is_employment_active,
    contract_type_code,
    hire_date,
    contract_start_date,
    contract_end_date,
    vacation_configuration_json,
    concurrency_token,
    created_utc,
    modified_utc,
    tenant_id
)
SELECT
    gen_random_uuid(),
    pending.id,
    'EMP-' || upper(substr(md5(pending.tenant_id::text || ':' || pending.public_id::text), 1, 10)),
    'EMP-' || upper(substr(md5(pending.tenant_id::text || ':' || pending.public_id::text), 1, 10)),
    CASE WHEN pending.is_active THEN 'ACTIVE' ELSE 'INACTIVE' END,
    pending.is_active,
    'UNSPECIFIED',
    pending.created_utc,
    pending.created_utc,
    NULL,
    NULL,
    gen_random_uuid(),
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc',
    pending.tenant_id
FROM pending;

WITH tenant_source AS (
    SELECT DISTINCT public_id AS tenant_id
    FROM companies
), catalog_source AS (
    SELECT * FROM (VALUES
        ('EmployeeStatus', 'ACTIVE', 'Activo', 10),
        ('EmployeeStatus', 'INACTIVE', 'Inactivo', 20),
        ('EmploymentContractType', 'UNSPECIFIED', 'No especificado', 10),
        ('EmploymentWorkday', 'FULL_TIME', 'Tiempo completo', 10),
        ('EmploymentPayrollType', 'MONTHLY', 'Mensual', 10),
        ('EmploymentIncomeType', 'BASE_SALARY', 'Salario base', 10),
        ('EmploymentSalaryRubric', 'ORDINARY', 'Ordinario', 10),
        ('EmploymentPayPeriod', 'MONTHLY', 'Mensual', 10),
        ('EmploymentBenefitType', 'VACATION', 'Vacaciones', 10),
        ('EmploymentPaymentMethod', 'BANK_TRANSFER', 'Transferencia bancaria', 10),
        ('EmploymentAuthorizationSubstitutionType', 'GENERAL', 'General', 10)
    ) AS source(category, code, name, sort_order)
)
INSERT INTO personnel_catalog_items (
    public_id,
    category,
    code,
    normalized_code,
    name,
    normalized_name,
    is_system,
    is_active,
    sort_order,
    concurrency_token,
    created_utc,
    modified_utc,
    tenant_id
)
SELECT
    gen_random_uuid(),
    catalog.category,
    catalog.code,
    upper(catalog.code),
    catalog.name,
    upper(catalog.name),
    true,
    true,
    catalog.sort_order,
    gen_random_uuid(),
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc',
    tenant.tenant_id
FROM tenant_source tenant
CROSS JOIN catalog_source catalog
ON CONFLICT (tenant_id, category, normalized_code) DO UPDATE
SET name = EXCLUDED.name,
    normalized_name = EXCLUDED.normalized_name,
    is_system = EXCLUDED.is_system,
    is_active = EXCLUDED.is_active,
    sort_order = EXCLUDED.sort_order,
    modified_utc = EXCLUDED.modified_utc;
