CREATE TABLE IF NOT EXISTS personnel_file_employee_profiles (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    personnel_file_id bigint NOT NULL,
    employee_code character varying(80) NOT NULL,
    normalized_employee_code character varying(80) NOT NULL,
    employment_status_code character varying(80) NOT NULL,
    is_employment_active boolean NOT NULL,
    contract_type_code character varying(80) NOT NULL,
    hire_date timestamp with time zone NOT NULL,
    retirement_category_code character varying(80),
    retirement_reason_code character varying(80),
    retirement_notes character varying(2000),
    retirement_date timestamp with time zone,
    workday_code character varying(80),
    payroll_type_code character varying(80),
    position_slot_public_id uuid,
    job_profile_public_id uuid,
    org_unit_public_id uuid,
    work_center_public_id uuid,
    cost_center_public_id uuid,
    contract_start_date timestamp with time zone,
    contract_end_date timestamp with time zone,
    vacation_configuration_json jsonb,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_personnel_file_employee_profiles PRIMARY KEY (id),
    CONSTRAINT fk_personnel_file_employee_profiles__personnel_file
        FOREIGN KEY (personnel_file_id)
        REFERENCES personnel_files (id)
        ON DELETE CASCADE,
    CONSTRAINT ck_personnel_file_employee_profiles__contract_dates
        CHECK (contract_end_date IS NULL OR contract_start_date IS NULL OR contract_end_date >= contract_start_date)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_employee_profiles__public_id
    ON personnel_file_employee_profiles (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_employee_profiles__tenant_employee_code
    ON personnel_file_employee_profiles (tenant_id, normalized_employee_code);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_employee_profiles__tenant_file
    ON personnel_file_employee_profiles (tenant_id, personnel_file_id);

