(
    id bigint generated by default as identity,
    field_key character varying(150) not null,
    normalized_field_key character varying(150) not null,
    resource_key character varying(100) not null,
    normalized_resource_key character varying(100) not null,
    property_name character varying(100) not null,
    normalized_property_name character varying(100) not null,
    display_name character varying(120) not null,
    is_configurable boolean not null,
    is_sensitive boolean not null,
    data_type character varying(40) not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_field_catalog primary key (id)
);

create table if not exists role_field_permissions
(
    id bigint generated by default as identity,
    tenant_id uuid not null,
    role_id bigint not null,
    field_key character varying(150) not null,
    normalized_field_key character varying(150) not null,
    is_visible boolean not null,
    is_editable boolean not null,
    is_required boolean not null,
    is_masked boolean not null,
    updated_by_user_id uuid not null,
    updated_at_utc timestamp with time zone not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_role_field_permissions primary key (id),
    constraint fk_role_field_permissions__iam_roles__role_id
        foreign key (role_id) references iam_roles (id) on delete cascade
);

create table if not exists field_permission_audit_logs
(
    id bigint generated by default as identity,
    tenant_id uuid not null,
    role_public_id uuid not null,
    field_key character varying(150) not null,
    normalized_field_key character varying(150) not null,
    changed_by_user_id uuid not null,
    before_json jsonb,
    after_json jsonb not null,
    changed_at_utc timestamp with time zone not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_field_permission_audit_logs primary key (id)
);

create unique index if not exists uq_auth_users__public_id
    on auth_users (public_id);

