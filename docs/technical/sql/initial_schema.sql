    on rbac_permission_audit_logs (tenant_id, role_public_id, changed_at_utc);

create index if not exists ix_rbac_permission_audit_logs__tenant_resource_changed_at
    on rbac_permission_audit_logs (tenant_id, normalized_resource_key, changed_at_utc);

create unique index if not exists uq_field_catalog__normalized_field_key
    on field_catalog (normalized_field_key);

create index if not exists ix_field_catalog__resource_configurable
    on field_catalog (normalized_resource_key, is_configurable);

create unique index if not exists uq_role_field_permissions__tenant_role_field
    on role_field_permissions (tenant_id, role_id, normalized_field_key);

create index if not exists ix_role_field_permissions__tenant_role
    on role_field_permissions (tenant_id, role_id);

create index if not exists ix_field_permission_audit_logs__tenant_role_changed_at
    on field_permission_audit_logs (tenant_id, role_public_id, changed_at_utc);

create index if not exists ix_field_permission_audit_logs__tenant_field_changed_at
    on field_permission_audit_logs (tenant_id, normalized_field_key, changed_at_utc);

create unique index if not exists uq_user_companies__primary_user
    on user_companies (user_id)
    where is_primary = true;

create unique index if not exists uq_user_companies__user_company
    on user_companies (user_id, company_id);

create index if not exists ix_user_companies__company_status_role
    on user_companies (company_id, status, role_id);

create unique index if not exists uq_company_invitation_tokens__token_hash
    on company_invitation_tokens (token_hash);

create index if not exists ix_company_invitation_tokens__user_company
    on company_invitation_tokens (user_id, company_id);

