    normalized_action character varying(100),
    field_name character varying(100),
    normalized_field_name character varying(100),
    field_access character varying(20),
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_iam_permissions primary key (id)
);

create table if not exists iam_roles
(
    id bigint generated by default as identity,
    public_id uuid not null,
    tenant_id uuid not null,
    name character varying(100) not null,
    normalized_name character varying(100) not null,
    description character varying(500),
    is_system_role boolean not null default false,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_iam_roles primary key (id)
);

create table if not exists user_companies
(
    id bigint generated by default as identity,
    user_id bigint not null,
    company_id bigint not null,
    role_id bigint not null,
    is_primary boolean not null,
    status character varying(30) not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_user_companies primary key (id),
    constraint fk_user_companies__auth_users
        foreign key (user_id) references auth_users (id) on delete cascade,
    constraint fk_user_companies__companies
        foreign key (company_id) references companies (id) on delete cascade,
    constraint fk_user_companies__iam_roles
        foreign key (role_id) references iam_roles (id) on delete restrict
);

create table if not exists company_invitation_tokens
(
    id bigint generated by default as identity,
    user_id bigint not null,
    company_id bigint not null,
    token_hash character varying(128) not null,
    expiration_utc timestamp with time zone not null,
    is_used boolean not null default false,
    revoked_utc timestamp with time zone,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_company_invitation_tokens primary key (id),
    constraint fk_company_invitation_tokens__auth_users
        foreign key (user_id) references auth_users (id) on delete cascade,
    constraint fk_company_invitation_tokens__companies
        foreign key (company_id) references companies (id) on delete cascade
);

create table if not exists iam_users
(
    id bigint generated by default as identity,
    public_id uuid not null,
    tenant_id uuid not null,
    first_name character varying(100) not null,
    last_name character varying(100) not null,
    email character varying(320) not null,
    normalized_email character varying(320) not null,
    is_active boolean not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_iam_users primary key (id)
);

create table if not exists iam_role_permission_assignments
(
    id bigint generated by default as identity,
    tenant_id uuid not null,
    role_id bigint not null,
    permission_id bigint not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_iam_role_permission_assignments primary key (id),
    constraint fk_iam_role_perm__iam_roles__role_id
        foreign key (role_id) references iam_roles (id) on delete cascade,
    constraint fk_iam_role_perm__iam_permissions__permission_id
        foreign key (permission_id) references iam_permissions (id) on delete cascade
);

create table if not exists iam_user_role_assignments
(
    id bigint generated by default as identity,
    tenant_id uuid not null,
    user_id bigint not null,
    role_id bigint not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_iam_user_role_assignments primary key (id),
    constraint fk_iam_user_role__iam_users__user_id
        foreign key (user_id) references iam_users (id) on delete cascade,
    constraint fk_iam_user_role__iam_roles__role_id
        foreign key (role_id) references iam_roles (id) on delete cascade
);

create table if not exists rbac_resource_catalog
(
    resource_key character varying(100) not null,
    normalized_resource_key character varying(100) not null,
    display_name character varying(120) not null,
    is_active boolean not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_rbac_resource_catalog primary key (resource_key)
);

create table if not exists rbac_permission_audit_logs
(
    id bigint generated by default as identity,
    tenant_id uuid not null,
    role_public_id uuid not null,
    resource_key character varying(100) not null,
    normalized_resource_key character varying(100) not null,
    changed_by_user_id uuid not null,
    change_type character varying(20) not null,
    before_json jsonb not null,
    after_json jsonb not null,
    changed_at_utc timestamp with time zone not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_rbac_permission_audit_logs primary key (id)
);

