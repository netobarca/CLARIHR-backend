    on iam_roles (tenant_id, normalized_name);

create index if not exists ix_iam_roles__tenant_name
    on iam_roles (tenant_id, name);

create unique index if not exists uq_iam_users__tenant_email
    on iam_users (tenant_id, normalized_email);

create index if not exists ix_iam_users__tenant_name
    on iam_users (tenant_id, last_name, first_name);

create unique index if not exists uq_iam_role_perm__tenant_role_perm
    on iam_role_permission_assignments (tenant_id, role_id, permission_id);

create unique index if not exists uq_iam_user_role__tenant_user_role
    on iam_user_role_assignments (tenant_id, user_id, role_id);

