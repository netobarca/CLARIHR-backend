create table if not exists auth_users
(
    id bigint generated by default as identity,
    public_id uuid not null,
    first_name character varying(100) not null,
    last_name character varying(100) not null,
    email character varying(320) not null,
    normalized_email character varying(320) not null,
    password_hash character varying(512),
    auth_provider character varying(30) not null,
    provider_user_id character varying(200),
    country character varying(100),
    source character varying(100),
    status character varying(30) not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_auth_users primary key (id)
);

create table if not exists companies
(
    id bigint generated by default as identity,
    public_id uuid not null,
    name character varying(150) not null,
    slug character varying(120) not null,
    status character varying(30) not null,
    created_by_user_public_id uuid not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_companies primary key (id)
);

create table if not exists company_subscriptions
(
    id bigint generated by default as identity,
    company_id bigint not null,
    plan_code character varying(40) not null,
    status character varying(30) not null,
    start_date_utc timestamp with time zone not null,
    end_date_utc timestamp with time zone,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
    constraint pk_company_subscriptions primary key (id),
    constraint fk_company_subscriptions__companies
        foreign key (company_id) references companies (id) on delete cascade
);

create table if not exists plan_entitlements
(
    id bigint generated by default as identity,
    plan_code character varying(40) not null,
    module_key character varying(60) not null,
    is_enabled boolean not null,
    created_utc timestamp with time zone not null,
    modified_utc timestamp with time zone,
