CREATE TABLE IF NOT EXISTS personnel_file_performance_evaluations (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    personnel_file_id bigint NOT NULL,
    evaluator_name character varying(200) NOT NULL,
    evaluation_date_utc timestamp with time zone NOT NULL,
    score numeric(18,2),
    qualitative_score_code character varying(80),
    comment character varying(2000),
    source_system character varying(80),
    source_reference character varying(120),
    source_synced_utc timestamp with time zone,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_personnel_file_performance_evaluations PRIMARY KEY (id),
    CONSTRAINT fk_personnel_file_performance_evaluations__personnel_file
        FOREIGN KEY (personnel_file_id)
        REFERENCES personnel_files (id)
        ON DELETE CASCADE
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_performance_evaluations__public_id
    ON personnel_file_performance_evaluations (public_id);

CREATE INDEX IF NOT EXISTS ix_personnel_file_performance_evaluations__tenant_file_date
    ON personnel_file_performance_evaluations (tenant_id, personnel_file_id, evaluation_date_utc);

