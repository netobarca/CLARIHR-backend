WITH tenants AS (
    SELECT DISTINCT tenant_id
    FROM iam_roles
),
permission_source AS (
    SELECT
        tenant_id,
        code,
        upper(code) AS normalized_code,
        name,
        description,
        module_name,
        upper(module_name) AS normalized_module,
        screen_name,
        upper(screen_name) AS normalized_screen,
        action_name,
        upper(action_name) AS normalized_action
    FROM tenants
    CROSS JOIN (
        VALUES
            ('OrgStructureCatalogs.Read', 'Read Org Structure Catalogs', 'Read organization structure catalogs.', 'ORG_STRUCTURE_CATALOGS', 'OrgStructureCatalogs', 'Read'),
            ('OrgStructureCatalogs.Admin', 'Admin Org Structure Catalogs', 'Manage organization structure catalogs.', 'ORG_STRUCTURE_CATALOGS', 'OrgStructureCatalogs', 'Manage')
    ) AS source(code, name, description, module_name, screen_name, action_name)
)
