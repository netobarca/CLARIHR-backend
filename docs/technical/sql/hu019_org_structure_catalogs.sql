INSERT INTO org_unit_type_catalog_items (
    public_id,
    tenant_id,
    code,
    normalized_code,
    name,
    normalized_name,
    description,
    sort_order,
    is_active,
    concurrency_token,
    created_utc,
    modified_utc
)
SELECT
    gen_random_uuid(),
    source.tenant_id,
    source.code,
    source.normalized_code,
    source.name,
    source.normalized_name,
    NULL,
    source.sort_order,
    true,
    gen_random_uuid(),
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc'
FROM (
    SELECT DISTINCT
        unit.tenant_id,
        btrim(unit.unit_type) AS code,
        upper(btrim(unit.unit_type)) AS normalized_code,
        btrim(unit.unit_type) AS name,
        upper(btrim(unit.unit_type)) AS normalized_name,
        CASE upper(btrim(unit.unit_type))
            WHEN 'DIRECCION' THEN 10
            WHEN 'GERENCIA' THEN 20
            WHEN 'DEPARTAMENTO' THEN 30
            WHEN 'COORDINACION' THEN 40
            WHEN 'UNIDAD' THEN 50
            WHEN 'OTRO' THEN 999
            ELSE 999
        END AS sort_order
    FROM org_units unit
    WHERE unit.unit_type IS NOT NULL
      AND btrim(unit.unit_type) <> ''
) AS source
WHERE NOT EXISTS (
    SELECT 1
    FROM org_unit_type_catalog_items existing
    WHERE existing.tenant_id = source.tenant_id
      AND existing.normalized_code = source.normalized_code
);

UPDATE org_units unit
SET org_unit_type_catalog_item_id = catalog.id
FROM org_unit_type_catalog_items catalog
WHERE unit.org_unit_type_catalog_item_id IS NULL
  AND catalog.tenant_id = unit.tenant_id
  AND catalog.normalized_code = upper(btrim(unit.unit_type));

UPDATE org_units unit
SET org_unit_type_catalog_item_id = fallback.id
FROM (
    SELECT tenant_id, MIN(id) AS id
    FROM org_unit_type_catalog_items
    GROUP BY tenant_id
) AS fallback
WHERE unit.org_unit_type_catalog_item_id IS NULL
  AND fallback.tenant_id = unit.tenant_id;

DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'public'
          AND table_name = 'org_units'
          AND column_name = 'unit_type')
    THEN
        ALTER TABLE org_units
            ALTER COLUMN org_unit_type_catalog_item_id SET NOT NULL;
    END IF;
END$$;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_org_units__org_unit_type_catalog_item')
    THEN
        ALTER TABLE org_units
            ADD CONSTRAINT fk_org_units__org_unit_type_catalog_item
                FOREIGN KEY (org_unit_type_catalog_item_id)
                REFERENCES org_unit_type_catalog_items (id)
                ON DELETE RESTRICT;
    END IF;
END$$;

DO $$
BEGIN
