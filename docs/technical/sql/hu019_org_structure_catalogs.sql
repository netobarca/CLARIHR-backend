INSERT INTO iam_role_permission_assignments (
    role_id,
    permission_id,
    created_utc,
    modified_utc,
    tenant_id
)
SELECT
    role.id,
    target_permission.id,
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc',
    role.tenant_id
FROM iam_roles role
JOIN iam_role_permission_assignments manage_assignment
    ON manage_assignment.role_id = role.id
   AND manage_assignment.tenant_id = role.tenant_id
JOIN iam_permissions manage_permission
    ON manage_permission.id = manage_assignment.permission_id
   AND manage_permission.tenant_id = role.tenant_id
JOIN iam_permissions target_permission
    ON target_permission.tenant_id = role.tenant_id
   AND target_permission.normalized_code IN ('ORGSTRUCTURECATALOGS.READ', 'ORGSTRUCTURECATALOGS.ADMIN')
WHERE manage_permission.normalized_code = 'IAM.ADMINISTRATION.MANAGE'
  AND NOT EXISTS (
      SELECT 1
      FROM iam_role_permission_assignments existing
      WHERE existing.role_id = role.id
        AND existing.permission_id = target_permission.id
  );
