-- HU-018 - Competency framework (occupational pyramid, conducts, matrix by job profile)

CREATE TABLE IF NOT EXISTS occupational_pyramid_levels (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    code character varying(50) NOT NULL,
    normalized_code character varying(50) NOT NULL,
    name character varying(120) NOT NULL,
    normalized_name character varying(120) NOT NULL,
    level_order integer NOT NULL,
    description character varying(500),
    is_active boolean NOT NULL,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_occupational_pyramid_levels PRIMARY KEY (id),
    CONSTRAINT ck_occupational_pyramid_levels__level_order CHECK (level_order > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_occupational_pyramid_levels__public_id
    ON occupational_pyramid_levels (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_occupational_pyramid_levels__tenant_code
    ON occupational_pyramid_levels (tenant_id, normalized_code);

CREATE UNIQUE INDEX IF NOT EXISTS uq_occupational_pyramid_levels__tenant_level_order
    ON occupational_pyramid_levels (tenant_id, level_order);

CREATE INDEX IF NOT EXISTS ix_occupational_pyramid_levels__tenant_active
    ON occupational_pyramid_levels (tenant_id, is_active);

CREATE INDEX IF NOT EXISTS ix_occupational_pyramid_levels__tenant_name
    ON occupational_pyramid_levels (tenant_id, normalized_name);

CREATE TABLE IF NOT EXISTS competency_conducts (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    competency_catalog_item_id bigint NOT NULL,
    competency_type_catalog_item_id bigint NOT NULL,
    behavior_level_catalog_item_id bigint NOT NULL,
    description character varying(1000) NOT NULL,
    normalized_description character varying(1000) NOT NULL,
    sort_order integer NOT NULL,
    is_active boolean NOT NULL,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_competency_conducts PRIMARY KEY (id),
    CONSTRAINT fk_competency_conducts__competency_catalog_item FOREIGN KEY (competency_catalog_item_id) REFERENCES job_catalog_items (id) ON DELETE RESTRICT,
    CONSTRAINT fk_competency_conducts__competency_type_catalog_item FOREIGN KEY (competency_type_catalog_item_id) REFERENCES job_catalog_items (id) ON DELETE RESTRICT,
    CONSTRAINT fk_competency_conducts__behavior_level_catalog_item FOREIGN KEY (behavior_level_catalog_item_id) REFERENCES job_catalog_items (id) ON DELETE RESTRICT,
    CONSTRAINT ck_competency_conducts__sort_order CHECK (sort_order >= 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_competency_conducts__public_id
    ON competency_conducts (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_competency_conducts__tenant_competency_type_level_desc
    ON competency_conducts (
        tenant_id,
        competency_catalog_item_id,
        competency_type_catalog_item_id,
        behavior_level_catalog_item_id,
        normalized_description
    );

CREATE INDEX IF NOT EXISTS ix_competency_conducts__tenant_competency_type_level
    ON competency_conducts (tenant_id, competency_catalog_item_id, competency_type_catalog_item_id, behavior_level_catalog_item_id);

CREATE INDEX IF NOT EXISTS ix_competency_conducts__tenant_active
    ON competency_conducts (tenant_id, is_active);

CREATE TABLE IF NOT EXISTS competency_conduct_behaviors (
