WITH tenant_source AS (
    SELECT DISTINCT company.public_id AS tenant_id
    FROM companies company
),
permission_source AS (
    SELECT
        source.tenant_id,
        item.code,
        item.name,
        item.description,
        item.module_name,
        item.screen_name,
        item.action_name
    FROM tenant_source source
    CROSS JOIN (
        VALUES
            ('CompetencyFramework.Read', 'Read Competency Framework', 'Read competency framework resources.', 'COMPETENCY_FRAMEWORK', 'CompetencyFramework', 'Read'),
            ('CompetencyFramework.Admin', 'Admin Competency Framework', 'Manage competency framework resources.', 'COMPETENCY_FRAMEWORK', 'CompetencyFramework', 'Manage')
    ) AS item(code, name, description, module_name, screen_name, action_name)
)
INSERT INTO iam_permissions (
    public_id,
    tenant_id,
    code,
    normalized_code,
    name,
    description,
    module,
    normalized_module,
    screen,
    normalized_screen,
    kind,
    action,
    normalized_action,
    field_name,
    normalized_field_name,
    field_access,
    created_utc,
    modified_utc
)
SELECT
    (
        SUBSTRING(MD5(source.tenant_id::text || ':' || UPPER(TRIM(source.code)) || ':public'), 1, 8) || '-' ||
        SUBSTRING(MD5(source.tenant_id::text || ':' || UPPER(TRIM(source.code)) || ':public'), 9, 4) || '-' ||
        SUBSTRING(MD5(source.tenant_id::text || ':' || UPPER(TRIM(source.code)) || ':public'), 13, 4) || '-' ||
        SUBSTRING(MD5(source.tenant_id::text || ':' || UPPER(TRIM(source.code)) || ':public'), 17, 4) || '-' ||
        SUBSTRING(MD5(source.tenant_id::text || ':' || UPPER(TRIM(source.code)) || ':public'), 21, 12)
    )::uuid,
    source.tenant_id,
    source.code,
    UPPER(TRIM(source.code)),
    source.name,
    source.description,
    source.module_name,
    UPPER(TRIM(source.module_name)),
    source.screen_name,
    UPPER(TRIM(source.screen_name)),
    'ScreenAction',
    source.action_name,
    UPPER(TRIM(source.action_name)),
    NULL,
    NULL,
    NULL,
    NOW() AT TIME ZONE 'utc',
    NOW() AT TIME ZONE 'utc'
FROM permission_source source
ON CONFLICT (tenant_id, normalized_code) DO UPDATE
SET code = EXCLUDED.code,
    name = EXCLUDED.name,
    description = EXCLUDED.description,
    module = EXCLUDED.module,
    normalized_module = EXCLUDED.normalized_module,
    screen = EXCLUDED.screen,
    normalized_screen = EXCLUDED.normalized_screen,
    kind = EXCLUDED.kind,
    action = EXCLUDED.action,
    normalized_action = EXCLUDED.normalized_action,
    modified_utc = EXCLUDED.modified_utc;

INSERT INTO iam_role_permission_assignments (
    tenant_id,
    role_id,
    permission_id,
    created_utc,
    modified_utc
)
SELECT
    role.tenant_id,
    role.id,
    target_permission.id,
    NOW() AT TIME ZONE 'utc',
    NOW() AT TIME ZONE 'utc'
FROM iam_roles role
JOIN iam_permissions manage_permission
    ON manage_permission.tenant_id = role.tenant_id
   AND manage_permission.normalized_code = 'IAM.ADMINISTRATION.MANAGE'
JOIN iam_role_permission_assignments manage_assignment
    ON manage_assignment.tenant_id = role.tenant_id
   AND manage_assignment.role_id = role.id
   AND manage_assignment.permission_id = manage_permission.id
JOIN iam_permissions target_permission
    ON target_permission.tenant_id = role.tenant_id
   AND target_permission.normalized_code IN ('COMPETENCYFRAMEWORK.READ', 'COMPETENCYFRAMEWORK.ADMIN')
WHERE NOT EXISTS (
    SELECT 1
    FROM iam_role_permission_assignments existing
    WHERE existing.tenant_id = role.tenant_id
      AND existing.role_id = role.id
      AND existing.permission_id = target_permission.id
);
