-- HU-020 - Position description catalogs (i-xiv)

CREATE TABLE IF NOT EXISTS position_description_catalog_items (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    tenant_id uuid NOT NULL,
    catalog_type character varying(50) NOT NULL,
    code character varying(50) NOT NULL,
    normalized_code character varying(50) NOT NULL,
    name character varying(150) NOT NULL,
    normalized_name character varying(150) NOT NULL,
    description character varying(500),
    sort_order integer NOT NULL,
    is_active boolean NOT NULL,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    CONSTRAINT pk_position_description_catalog_items PRIMARY KEY (id),
    CONSTRAINT ck_position_description_catalog_items__sort_order CHECK (sort_order >= 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_position_description_catalog_items__public_id
    ON position_description_catalog_items (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_position_description_catalog_items__tenant_type_code
    ON position_description_catalog_items (tenant_id, catalog_type, normalized_code);

CREATE INDEX IF NOT EXISTS ix_position_description_catalog_items__tenant_type_name
    ON position_description_catalog_items (tenant_id, catalog_type, normalized_name);

CREATE INDEX IF NOT EXISTS ix_position_description_catalog_items__tenant_type_active
    ON position_description_catalog_items (tenant_id, catalog_type, is_active);

CREATE TABLE IF NOT EXISTS position_category_classifications (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    tenant_id uuid NOT NULL,
    code character varying(50) NOT NULL,
    normalized_code character varying(50) NOT NULL,
    name character varying(150) NOT NULL,
    normalized_name character varying(150) NOT NULL,
    description character varying(500),
    position_function_catalog_item_id bigint NOT NULL,
    position_contract_catalog_item_id bigint NOT NULL,
    org_unit_type_catalog_item_id bigint NOT NULL,
    sort_order integer NOT NULL,
    is_active boolean NOT NULL,
    concurrency_token uuid NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    CONSTRAINT pk_position_category_classifications PRIMARY KEY (id),
    CONSTRAINT ck_position_category_classifications__sort_order CHECK (sort_order >= 0),
    CONSTRAINT fk_position_category_classifications__position_function_catalog_item
        FOREIGN KEY (position_function_catalog_item_id) REFERENCES position_description_catalog_items (id) ON DELETE RESTRICT,
    CONSTRAINT fk_position_category_classifications__position_contract_catalog_item
        FOREIGN KEY (position_contract_catalog_item_id) REFERENCES position_description_catalog_items (id) ON DELETE RESTRICT,
    CONSTRAINT fk_position_category_classifications__org_unit_type_catalog_item
        FOREIGN KEY (org_unit_type_catalog_item_id) REFERENCES org_unit_type_catalog_items (id) ON DELETE RESTRICT
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_position_category_classifications__public_id
    ON position_category_classifications (public_id);

CREATE UNIQUE INDEX IF NOT EXISTS uq_position_category_classifications__tenant_code
    ON position_category_classifications (tenant_id, normalized_code);

CREATE UNIQUE INDEX IF NOT EXISTS uq_position_category_classifications__tenant_axes
    ON position_category_classifications (
        tenant_id,
        position_function_catalog_item_id,
        position_contract_catalog_item_id,
        org_unit_type_catalog_item_id
    );

CREATE INDEX IF NOT EXISTS ix_position_category_classifications__tenant_name
    ON position_category_classifications (tenant_id, normalized_name);

CREATE INDEX IF NOT EXISTS ix_position_category_classifications__tenant_active
    ON position_category_classifications (tenant_id, is_active);

CREATE TABLE IF NOT EXISTS position_categories (
