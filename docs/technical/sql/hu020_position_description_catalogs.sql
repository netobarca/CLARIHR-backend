    action = EXCLUDED.action,
    normalized_action = EXCLUDED.normalized_action,
    field_name = EXCLUDED.field_name,
    normalized_field_name = EXCLUDED.normalized_field_name,
    field_access = EXCLUDED.field_access,
    modified_utc = EXCLUDED.modified_utc;

WITH admin_manage_permissions AS (
    SELECT tenant_id, id
    FROM iam_permissions
    WHERE normalized_code = 'IAM.ADMINISTRATION.MANAGE'
),
admin_roles AS (
    SELECT DISTINCT
        assignment.tenant_id,
        assignment.role_id
    FROM iam_role_permission_assignments assignment
    JOIN admin_manage_permissions permission
      ON permission.tenant_id = assignment.tenant_id
     AND permission.id = assignment.permission_id
),
module_permissions AS (
    SELECT tenant_id, id
    FROM iam_permissions
    WHERE normalized_code IN ('POSITIONDESCRIPTIONCATALOGS.READ', 'POSITIONDESCRIPTIONCATALOGS.ADMIN')
)
INSERT INTO iam_role_permission_assignments (
    tenant_id,
    role_id,
    permission_id,
    created_utc,
    modified_utc
)
SELECT
    role.tenant_id,
    role.role_id,
    permission.id,
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc'
FROM admin_roles role
JOIN module_permissions permission
  ON permission.tenant_id = role.tenant_id
WHERE NOT EXISTS (
    SELECT 1
    FROM iam_role_permission_assignments existing
    WHERE existing.tenant_id = role.tenant_id
      AND existing.role_id = role.role_id
      AND existing.permission_id = permission.id
);
