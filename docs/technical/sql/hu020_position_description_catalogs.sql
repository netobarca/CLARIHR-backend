WITH tenants AS (
    SELECT DISTINCT tenant_id
    FROM iam_roles
),
permission_source AS (
    SELECT
        tenant_id,
        code,
        upper(code) AS normalized_code,
        name,
        description,
        module_name,
        upper(module_name) AS normalized_module,
        screen_name,
        upper(screen_name) AS normalized_screen,
        action_name,
        upper(action_name) AS normalized_action
    FROM tenants
    CROSS JOIN (
        VALUES
            ('PositionDescriptionCatalogs.Read', 'Read Position Description Catalogs', 'Read position description catalogs.', 'POSITION_DESCRIPTION_CATALOGS', 'PositionDescriptionCatalogs', 'Read'),
            ('PositionDescriptionCatalogs.Admin', 'Admin Position Description Catalogs', 'Manage position description catalogs.', 'POSITION_DESCRIPTION_CATALOGS', 'PositionDescriptionCatalogs', 'Manage')
    ) AS source(code, name, description, module_name, screen_name, action_name)
)
INSERT INTO iam_permissions (
    public_id,
    tenant_id,
    code,
    normalized_code,
    name,
    description,
    module,
    normalized_module,
    screen,
    normalized_screen,
    kind,
    action,
    normalized_action,
    field_name,
    normalized_field_name,
    field_access,
    created_utc,
    modified_utc
)
SELECT
    gen_random_uuid(),
    source.tenant_id,
    source.code,
    source.normalized_code,
    source.name,
    source.description,
    source.module_name,
    source.normalized_module,
    source.screen_name,
    source.normalized_screen,
    'Action',
    source.action_name,
    source.normalized_action,
    NULL,
    NULL,
    NULL,
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc'
FROM permission_source source
ON CONFLICT (tenant_id, normalized_code) DO UPDATE
SET name = EXCLUDED.name,
    description = EXCLUDED.description,
