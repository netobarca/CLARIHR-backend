CREATE TABLE IF NOT EXISTS personnel_file_personnel_actions (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    public_id uuid NOT NULL,
    personnel_file_id bigint NOT NULL,
    action_type_code character varying(80) NOT NULL,
    action_status_code character varying(80) NOT NULL,
    action_date_utc timestamp with time zone NOT NULL,
    effective_from_utc timestamp with time zone,
    effective_to_utc timestamp with time zone,
    description character varying(2000),
    reference character varying(120),
    amount numeric(18,2),
    currency_code character varying(40),
    is_system_generated boolean NOT NULL,
    created_utc timestamp with time zone NOT NULL,
    modified_utc timestamp with time zone,
    tenant_id uuid NOT NULL,
    CONSTRAINT pk_personnel_file_personnel_actions PRIMARY KEY (id),
    CONSTRAINT fk_personnel_file_personnel_actions__personnel_file
        FOREIGN KEY (personnel_file_id)
        REFERENCES personnel_files (id)
        ON DELETE CASCADE
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_personnel_file_personnel_actions__public_id
    ON personnel_file_personnel_actions (public_id);

CREATE INDEX IF NOT EXISTS ix_personnel_file_personnel_actions__tenant_file_action_date
    ON personnel_file_personnel_actions (tenant_id, personnel_file_id, action_date_utc);

CREATE INDEX IF NOT EXISTS ix_personnel_file_personnel_actions__tenant_file_type_status
    ON personnel_file_personnel_actions (tenant_id, personnel_file_id, action_type_code, action_status_code);

