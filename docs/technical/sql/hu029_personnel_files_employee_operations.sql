WITH tenant_source AS (
    SELECT DISTINCT public_id AS tenant_id
    FROM companies
), catalog_source AS (
    SELECT * FROM (VALUES
        ('EmploymentPersonnelActionType', 'SALARY_INCREMENT', 'Incremento salarial', 10),
        ('EmploymentPersonnelActionType', 'CONTRACT_CHANGE', 'Cambio de contrato', 20),
        ('EmploymentPersonnelActionType', 'TRANSFER', 'Traslado', 30),
        ('EmploymentPersonnelActionStatus', 'APPLIED', 'Aplicado', 10),
        ('EmploymentPersonnelActionStatus', 'PENDING', 'Pendiente', 20),
        ('EmploymentPayrollTransactionType', 'EARNING', 'Ingreso', 10),
        ('EmploymentPayrollTransactionType', 'DEDUCTION', 'Descuento', 20),
        ('EmploymentAssetType', 'LAPTOP', 'Computadora', 10),
        ('EmploymentAccessLevel', 'STANDARD', 'Estandar', 10),
        ('EmploymentInsurance', 'MEDICAL', 'Seguro medico', 10),
        ('EmploymentInsuranceRange', 'FAMILY', 'Familiar', 10),
        ('EmploymentKinship', 'SPOUSE', 'Conyuge', 10),
        ('EmploymentMedicalClaimType', 'OUTPATIENT', 'Consulta externa', 10)
    ) AS source(category, code, name, sort_order)
)
INSERT INTO personnel_catalog_items (
    public_id,
    category,
    code,
    normalized_code,
    name,
    normalized_name,
    is_system,
    is_active,
    sort_order,
    concurrency_token,
    created_utc,
    modified_utc,
    tenant_id
)
SELECT
    gen_random_uuid(),
    catalog.category,
    catalog.code,
    upper(catalog.code),
    catalog.name,
    upper(catalog.name),
    true,
    true,
    catalog.sort_order,
    gen_random_uuid(),
    now() AT TIME ZONE 'utc',
    now() AT TIME ZONE 'utc',
    tenant.tenant_id
FROM tenant_source tenant
CROSS JOIN catalog_source catalog
ON CONFLICT (tenant_id, category, normalized_code) DO UPDATE
SET name = EXCLUDED.name,
    normalized_name = EXCLUDED.normalized_name,
    is_system = EXCLUDED.is_system,
    is_active = EXCLUDED.is_active,
    sort_order = EXCLUDED.sort_order,
    modified_utc = EXCLUDED.modified_utc;
